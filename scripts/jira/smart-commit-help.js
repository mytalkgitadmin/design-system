#!/usr/bin/env node

console.log(`
╔═══════════════════════════════════════════════════════════════╗
║                 🎯 Jira Smart Commit 사용법                   ║
╚═══════════════════════════════════════════════════════════════╝

📝 기본 문법:
   git commit -m "AUDS-123 커밋 메시지"

   → Push 시 Jira 이슈 AUDS-123에 커밋 링크가 자동으로 추가됩니다.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

🔥 스마트 커밋 명령어:

┌─────────────┬──────────────────────────────────────────────────┐
│ 명령어      │ 설명                                             │
├─────────────┼──────────────────────────────────────────────────┤
│ #comment    │ Jira 이슈에 코멘트 추가                          │
│ #time       │ 작업 시간 기록 (예: 2h 30m)                      │
│ #start      │ 이슈를 "진행 중" 상태로 변경                     │
│ #resolve    │ 이슈를 "완료" 상태로 변경                        │
│ #close      │ 이슈를 "완료" 상태로 변경                        │
└─────────────┴──────────────────────────────────────────────────┘

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

💡 실전 예제:

1️⃣  단순 이슈 연결:
   git commit -m "AUDS-123 로그인 버튼 스타일 수정"

2️⃣  코멘트 추가:
   git commit -m "AUDS-456 #comment Redux 상태 관리로 리팩토링 완료"

3️⃣  작업 시간 기록:
   git commit -m "AUDS-789 #time 3h #comment API 통합 테스트 완료"

4️⃣  이슈 완료:
   git commit -m "AUDS-123 #close #comment 모든 테스트 통과, 배포 준비 완료"

5️⃣  여러 이슈 동시 처리:
   git commit -m "AUDS-123 AUDS-456 #comment 관련 이슈 일괄 수정"

6️⃣  복합 명령:
   git commit -m "AUDS-789 #time 2h 30m #comment 버그 수정 완료 #resolve"

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

⏱️  시간 형식:

   w = 주    (1주 = 5일)
   d = 일    (1일 = 8시간)
   h = 시간
   m = 분

   예시:
   #time 1w 2d 4h 30m    → 1주 2일 4시간 30분
   #time 3h 45m          → 3시간 45분
   #time 1d              → 1일 (8시간)

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

⚙️  동작 방식:

   1. 로컬 커밋 시 → Husky가 메시지 검증 (경고만 표시)
   2. Push 시 → GitHub Actions가 자동으로 Jira 업데이트
   3. Jira에서 → 커밋 링크 및 명령어 처리 결과 확인

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

🚨 주의사항:

   • Jira 이슈 키는 대문자로 작성 (AUDS-123 ✅, auds-123 ❌)
   • 명령어는 소문자 사용 (#comment ✅, #Comment ❌)
   • 여러 명령어 사용 시 공백으로 구분

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

📚 추가 정보:

   • GitHub Actions 워크플로우: .github/workflows/jira-smart-commit.yml
   • 처리 스크립트: scripts/jira/process-smart-commits.js
   • Workflow 설정: scripts/jira/jira-workflow-config.json

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

💬 문제 발생 시:

   1. GitHub Actions 로그 확인
   2. Jira API 권한 확인
   3. jira-workflow-config.json의 transition ID 확인

`);

